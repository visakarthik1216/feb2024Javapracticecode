package naveenSelenium;

public class Loops {

	public static void main(String[] args) {
		
		int i=1;
		while(i<=5)
		{
			System.out.println("i am a batman");
			i++;
		}
		
		int j=1;
		while(j<10)
		{
			System.out.println("i am a batman"+j);
			j++;
		}
		
		int k=10;
		while(k>0)
		{
			System.out.println(k);
			k--;
		}
		
		int x=10;
		do 
		{
			System.out.println(x);
			x--;
		}
		while(x>0);
		
		for (int p=10;p>0;p--)
		{
			System.out.println(p);
		}
	}

}
